/*==================================================================*
 *                     GPIO 模拟 I2C 时序函数                        *
 *==================================================================*/

/**
  * @brief  I2C 延时（简单的空循环延时）
  */
static void OLED_I2C_Delay(void)
{
    volatile uint32_t t = OLED_I2C_SPEED_DELAY;
    while (t--);
}

/**
  * @brief  I2C 起始信号
  *         SCL 高电平时，SDA 从高到低跳变
  */
static void OLED_I2C_Start(void)
{
    OLED_SDA_H();
    OLED_SCL_H();
    OLED_I2C_Delay();
    OLED_SDA_L();
    OLED_I2C_Delay();
    OLED_SCL_L();
}

/**
  * @brief  I2C 停止信号
  *         SCL 高电平时，SDA 从低到高跳变
  */
static void OLED_I2C_Stop(void)
{
    OLED_SDA_L();
    OLED_SCL_H();
    OLED_I2C_Delay();
    OLED_SDA_H();
    OLED_I2C_Delay();
}

/**
  * @brief  I2C 发送一个字节
  * @param  data  要发送的字节
  * @retval 0 收到 ACK（应答成功），1 收到 NACK（无应答）
  */
static uint8_t OLED_I2C_SendByte(uint8_t data)
{
    for (uint8_t i = 0; i < 8; i++)
    {
        if (data & 0x80)
            OLED_SDA_H();
        else
            OLED_SDA_L();
        OLED_I2C_Delay();
        OLED_SCL_H();
        OLED_I2C_Delay();
        OLED_SCL_L();
        data <<= 1;
    }
    // 释放 SDA，等待从机应答
    OLED_SDA_H();
    OLED_I2C_Delay();
    OLED_SCL_H();
    OLED_I2C_Delay();
    uint8_t ack = OLED_SDA_READ();  // 读取应答位
    OLED_SCL_L();
    return ack;  // 0 = ACK, 1 = NACK
}

/**
  * @brief  通过 GPIO 模拟 I2C 向 SSD1306 写入一个字节
  * @param  reg   0x00 表示命令，0x40 表示数据
  * @param  data  要写入的字节
  */
static void OLED_I2C_Write(uint8_t reg, uint8_t data)
{
    OLED_I2C_Start();
    OLED_I2C_SendByte(OLED_I2C_ADDR);   // 发送设备地址 + 写
    OLED_I2C_SendByte(reg);             // 发送控制字节（命令/数据）
    OLED_I2C_SendByte(data);            // 发送数据
    OLED_I2C_Stop();
}

