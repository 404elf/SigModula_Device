/**
  * @brief  设置光标（页地址 + 列地址）
  * @param  x  列坐标（0 ~ OLED_WIDTH - 1）
  * @param  y  页坐标（0 ~ OLED_PAGES - 1）
  */
static void OLED_SetCursor(uint8_t x, uint8_t y)
{
    OLED_WriteCmd(0xB0 + y);                    // 设置页地址
    OLED_WriteCmd(0x10 | ((x >> 4) & 0x0F));    // 设置列地址高 4 位
    OLED_WriteCmd(x & 0x0F);                    // 设置列地址低 4 位
}

/**
  * @brief  GPIO 模拟 I2C 引脚初始化（推挽输出）
  *         将 SCL 和 SDA 配置为开漏输出（外部上拉）或推挽输出
  */
static void OLED_I2C_GPIO_Init(void)
{
    GPIO_InitTypeDef GPIO_InitStruct = {0};

    /* 使能 GPIO 时钟（根据端口自动判断） */
    if (OLED_I2C_SCL_Port == GPIOA || OLED_I2C_SDA_Port == GPIOA)
        __HAL_RCC_GPIOA_CLK_ENABLE();
    if (OLED_I2C_SCL_Port == GPIOB || OLED_I2C_SDA_Port == GPIOB)
        __HAL_RCC_GPIOB_CLK_ENABLE();
    if (OLED_I2C_SCL_Port == GPIOC || OLED_I2C_SDA_Port == GPIOC)
        __HAL_RCC_GPIOC_CLK_ENABLE();
    if (OLED_I2C_SCL_Port == GPIOD || OLED_I2C_SDA_Port == GPIOD)
        __HAL_RCC_GPIOD_CLK_ENABLE();

    /* 配置 SCL 引脚：推挽输出 */
    GPIO_InitStruct.Pin = OLED_I2C_SCL_Pin;
    GPIO_InitStruct.Mode = GPIO_MODE_OUTPUT_PP;
    GPIO_InitStruct.Pull = GPIO_PULLUP;
    GPIO_InitStruct.Speed = GPIO_SPEED_FREQ_HIGH;
    HAL_GPIO_Init(OLED_I2C_SCL_Port, &GPIO_InitStruct);

    /* 配置 SDA 引脚：推挽输出 */
    GPIO_InitStruct.Pin = OLED_I2C_SDA_Pin;
    HAL_GPIO_Init(OLED_I2C_SDA_Port, &GPIO_InitStruct);

    /* 初始状态：SCL 和 SDA 均为高电平（总线空闲） */
    OLED_SCL_H();
    OLED_SDA_H();
}

/**
  * @brief  SSD1306 初始化序列
  */
void OLED_Init(void)
{
#if (OLED_GPIO_INIT_ENABLE == 1)
    /* 自动初始化 I2C 引脚（推挽输出） */
    OLED_I2C_GPIO_Init();
#else
    /* 由用户在 CubeMX 中配置引脚，此处仅将 SCL/SDA 拉高（总线空闲态） */
    OLED_SCL_H();
    OLED_SDA_H();
#endif

    HAL_Delay(100);  // 等待上电稳定

    OLED_WriteCmd(0xAE);    // 关闭显示
    OLED_WriteCmd(0x20);    // 设置内存寻址模式
    OLED_WriteCmd(0x00);    // 水平寻址模式
    OLED_WriteCmd(0xB0);    // 设置页起始地址
    OLED_WriteCmd(0xC8);    // COM 扫描方向：从 COM[N-1] 到 COM0
    OLED_WriteCmd(0x00);    // 设置低列地址
    OLED_WriteCmd(0x10);    // 设置高列地址
    OLED_WriteCmd(0x40);    // 设置显示起始行
    OLED_WriteCmd(0x81);    // 设置对比度
    OLED_WriteCmd(0xFF);    // 对比度最大值
    OLED_WriteCmd(0xA1);    // 段重映射：列 127 映射到 SEG0
    OLED_WriteCmd(0xA6);    // 正常显示（非反色）
    OLED_WriteCmd(0xA8);    // 设置多路复用比
    OLED_WriteCmd(0x3F);    // 1/64 duty（128×64 下为 63）
    OLED_WriteCmd(0xA4);    // 全局显示：按 GDDRAM 内容显示
    OLED_WriteCmd(0xD3);    // 设置显示偏移
    OLED_WriteCmd(0x00);    // 偏移量为 0
    OLED_WriteCmd(0xD5);    // 设置振荡频率
    OLED_WriteCmd(0xF0);    // 默认值
    OLED_WriteCmd(0xD9);    // 设置预充电周期
    OLED_WriteCmd(0x22);    // 默认值
    OLED_WriteCmd(0xDA);    // 设置 COM 引脚配置
    OLED_WriteCmd(0x12);    // 128×64 配置
    OLED_WriteCmd(0xDB);    // 设置 VCOMH 电压
    OLED_WriteCmd(0x20);    // 默认值
    OLED_WriteCmd(0x8D);    // 启用电荷泵
    OLED_WriteCmd(0x14);    // 开启电荷泵
    OLED_WriteCmd(0xAF);    // 开启显示

    OLED_ClearBuffer();
    OLED_Refresh();
}

/**
  * @brief  将显存缓冲区刷新到 OLED
  */
void OLED_Refresh(void)
{
#if (OLED_ROTATE == OLED_ROTATE_180)
    /* 旋转 180°：从右下角开始反向发送 */
    for (uint8_t page = 0; page < OLED_PAGES; page++)
    {
        OLED_SetCursor(OLED_WIDTH - 1, page);
        for (uint8_t col = 0; col < OLED_WIDTH; col++)
        {
            uint16_t idx = page * OLED_WIDTH + (OLED_WIDTH - 1 - col);
            OLED_WriteData(OLED_Buffer[idx]);
        }
    }
#else
    /* 正常方向：逐页逐列发送 */
    for (uint8_t page = 0; page < OLED_PAGES; page++)
    {
        OLED_SetCursor(0, page);
        for (uint8_t col = 0; col < OLED_WIDTH; col++)
        {
            OLED_WriteData(OLED_Buffer[page * OLED_WIDTH + col]);
        }
    }
#endif
}

/**
  * @brief  清空显存缓冲区
  */
void OLED_ClearBuffer(void)
{
    for (uint16_t i = 0; i < sizeof(OLED_Buffer); i++)
    {
        OLED_Buffer[i] = 0x00;
    }
}

/**
  * @brief  全屏填充
  * @param  data  填充数据（0x00 黑，0xFF 白）
  */
void OLED_Fill(uint8_t data)
{
    for (uint16_t i = 0; i < sizeof(OLED_Buffer); i++)
    {
        OLED_Buffer[i] = data;
    }
    OLED_Refresh();
}

